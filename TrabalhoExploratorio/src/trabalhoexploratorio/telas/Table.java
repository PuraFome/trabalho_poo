    private void btAddPessoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddPessoaActionPerformed
        // popup para adicionar os dados na tabela 
        String id = JOptionPane.showInputDialog("Digite o ID:");
        String primeiroNome = JOptionPane.showInputDialog("Digite o primeiro nome:");
        String sobrenome = JOptionPane.showInputDialog("Digite o sobrenome:");
        String dataNascimento = JOptionPane.showInputDialog("Digite a data de nascimento:");

        // adicionando os dados na tabela
        Object linha[] = new Object[4];

        linha[0] = id;
        linha[1] = primeiroNome;
        linha[2] = sobrenome;
        linha[3] = dataNascimento;

        DefaultTableModel model = (DefaultTableModel) pessoas.getModel();
        model.addRow(linha);
        

    }//GEN-LAST:event_btAddPessoaActionPerformed

